public class Name {
    private String f,m,l;
    public Name(String f, String m, String l){
        this.f = f;
        this.m= m;
        this.l = l;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }
}
